# String ifadeler tek tırnak veya çift tırnak içerisinde yazılır.

# Kaçış karakteri(\) kullanılarak tek tırnak içerisinde yazma.
sonuc = 'Merhaba, İzmir\'e selamlar.'

# Çift tırnak içerisinde yazma.
sonuc = "Merhaba, İzmir'e selamlar"

# String ifadelerde çarpma işlemi.
sonuc = "Merhaba" * 2  # MerhabaMerhaba 

# String ifadelerde toplama işlemi.
sonuc = "Merhaba" + "İzmir" # Merhabaİzmir
