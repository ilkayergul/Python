#Ekrana yazdırma işlemi yapar.
print('İlkay Ergül')

#Tırnak işaretlerini virgül ile ayrılabilir. Boşluk görevi görür.
print('İlkay','Ergül') #İlkay Ergül

#Artı işareti birleştirme yapar.
print('İlkay'+'Ergül') #İlkayErgül

#String ve int ifadeler virgül ile ayrılarak yazılabilir.
print('Yaşım:',24) #Yaşım: 24

#Virgül koyulan kısımlara değer atama.
print('Adım','İlkay', sep='---') #Adım---İlkay

#Çıktının sonuna değer atama
print('Adım','İlkay', end='---') #Adım İlkay ---
