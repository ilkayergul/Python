# Sözlük -> Key, Value

nesne = {'Yukseklik':'20', 'Genislik':'10'}

# Key değeri üzerinden value değerine erişilir.
print(nesne['Yukseklik'])
print(nesne['Genislik'])

# Key değerine göre düzenleme yapılır.
nesne['Yukseklik'] = '30'

# Key değerini silme
del nesne['Yukseklik']

# Sözlüğü temizleme. İçerisini boşaltır. Bellekte adresini tutar.
nesne.clear()

# Sözlüğü silme.
del nesne










